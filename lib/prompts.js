/**
 * Получить промпт по типу команды
 * @param {string} command - Тип команды
 * @returns {string}
 */
function getPrompt(command) {
    const prompts = {
        create: CREATE_PROJECT_PROMPT,
        analyze: ANALYZE_PROMPT,
        refactor: REFACTOR_PROMPT,
        explain: EXPLAIN_PROMPT,
        test: TEST_PROMPT,
        doc: DOC_PROMPT,
        project: PROJECT_PROMPT,
        chat: CHAT_PROMPT,
        fix: FIX_PROMPT,
        security: SECURITY_PROMPT,
    };

    return prompts[command] || CHAT_PROMPT;
}

/**
 * Получить промпт для конкретной задачи
 * @param {string} task - Задача
 * @param {Object} context - Контекст
 * @returns {string}
 */
function buildPrompt(task, context = {}) {
    const basePrompt = context.systemPrompt || CHAT_PROMPT;
    
    let additionalContext = '';
    
    if (context.files) {
        additionalContext += '\n\n📁 Файлы проекта:\n';
        for (const file of context.files.slice(0, 10)) {
            additionalContext += `\n--- ${file.path} ---\n${file.content.slice(0, 2000)}\n`;
        }
    }
    
    if (context.fileContent) {
        additionalContext += `\n\n📄 Анализируемый код:\n\`\`\`\n${context.fileContent}\n\`\`\``;
    }
    
    return `${basePrompt}\n\n${additionalContext}\n\nЗадача: ${task}`;
}

